import 'dotenv/config';
import http from 'http';
import app from './app';
import { initializeSocket } from './lib/socket';
import { redisClient } from './lib/redis';
import { prisma } from './lib/prisma';
import logger from './lib/logger';

const PORT = parseInt(process.env.PORT || '3001', 10);

async function bootstrap() {
  try {
    // â”€â”€â”€ Test DB Connection â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    await prisma.$connect();
    logger.info('âœ… PostgreSQL connected');

    // â”€â”€â”€ Test Redis Connection â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    await redisClient.ping();
    logger.info('âœ… Redis connected');

    // â”€â”€â”€ Create HTTP Server â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    const server = http.createServer(app);

    // â”€â”€â”€ Initialize Socket.io â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    initializeSocket(server);
    logger.info('âœ… Socket.io initialized');

    // â”€â”€â”€ Start Server â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    server.listen(PORT, () => {
      logger.info(`ðŸš€ CompSense API running on port ${PORT}`);
      logger.info(`ðŸ“Š Environment: ${process.env.NODE_ENV || 'development'}`);
      logger.info(`ðŸŒ Frontend URL: ${process.env.FRONTEND_URL || 'http://localhost:5173'}`);
    });

    // â”€â”€â”€ Graceful Shutdown â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    const shutdown = async (signal: string) => {
      logger.info(`Received ${signal}. Shutting down gracefully...`);
      server.close(async () => {
        await prisma.$disconnect();
        await redisClient.quit();
        logger.info('Server closed.');
        process.exit(0);
      });
    };

    process.on('SIGTERM', () => shutdown('SIGTERM'));
    process.on('SIGINT', () => shutdown('SIGINT'));

  } catch (error) {
    logger.error('Failed to start server:', error);
    process.exit(1);
  }
}

bootstrap();
